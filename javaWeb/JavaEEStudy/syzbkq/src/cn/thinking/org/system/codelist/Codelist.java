package cn.thinking.org.system.codelist;

/**
 * 下拉列表框
 * @author Administrator
 *
 */
public class Codelist {
	private String id;//流水号
	private String codekind;//数据字典名称
	private String codename;//代码名称
	private String codevalue;//代码值
	private String codedescription;//字典描述信息
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCodekind() {
		return codekind;
	}
	public void setCodekind(String codekind) {
		this.codekind = codekind;
	}
	public String getCodename() {
		return codename;
	}
	public void setCodename(String codename) {
		this.codename = codename;
	}
	public String getCodevalue() {
		return codevalue;
	}
	public void setCodevalue(String codevalue) {
		this.codevalue = codevalue;
	}
	public String getCodedescription() {
		return codedescription;
	}
	public void setCodedescription(String codedescription) {
		this.codedescription = codedescription;
	}
}
