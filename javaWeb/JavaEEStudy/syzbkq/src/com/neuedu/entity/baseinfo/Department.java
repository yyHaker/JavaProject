package com.neuedu.entity.baseinfo;

import java.sql.Date;

import cn.thinking.common.page.Page;

public class Department {
	
	private int did;//人员组号
	private int parentdepartment;//上级部门号
	private String createtime;//创建时间
	private String dname;
	
	
	private Page page = new Page();


	public int getDid() {
		return did;
	}


	public void setDid(int did) {
		this.did = did;
	}


	public int getParentdepartment() {
		return parentdepartment;
	}


	public void setParentdepartment(int parentdepartment) {
		this.parentdepartment = parentdepartment;
	}


	public String getCreatetime() {
		return createtime;
	}


	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}


	public String getDname() {
		return dname;
	}


	public void setDname(String dname) {
		this.dname = dname;
	}


	public Page getPage() {
		return page;
	}


	public void setPage(Page page) {
		this.page = page;
	}
	
	
	
}
