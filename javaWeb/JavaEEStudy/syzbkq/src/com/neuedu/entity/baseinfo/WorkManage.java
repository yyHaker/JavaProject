package com.neuedu.entity.baseinfo;

import cn.thinking.common.page.Page;

/**
 * 班组排班管理实体类
 * @author Administrator
 *
 */
public class WorkManage {
	private String workmanage_id;     //班组排班管理主键id
	private String wdepartment;       //部门
	private String wname;             //班制名称
	private String wclass_name;       //班组名称
	private String wclass_group;      //班组人员
	private String wclass_description;//班制说明
	private Page page = new Page();
	public String getWorkmanage_id() {
		return workmanage_id;
	}
	public void setWorkmanage_id(String workmanage_id) {
		this.workmanage_id = workmanage_id;
	}
	public String getWdepartment() {
		return wdepartment;
	}
	public void setWdepartment(String wdepartment) {
		this.wdepartment = wdepartment;
	}
	public String getWname() {
		return wname;
	}
	public void setWname(String wname) {
		this.wname = wname;
	}
	public String getWclass_name() {
		return wclass_name;
	}
	public void setWclass_name(String wclass_name) {
		this.wclass_name = wclass_name;
	}
	public String getWclass_group() {
		return wclass_group;
	}
	public void setWclass_group(String wclass_group) {
		this.wclass_group = wclass_group;
	}
	public String getWclass_description() {
		return wclass_description;
	}
	public void setWclass_description(String wclass_description) {
		this.wclass_description = wclass_description;
	}
	public Page getPage() {
		return page;
	}
	public void setPage(Page page) {
		this.page = page;
	}
	
	

}
