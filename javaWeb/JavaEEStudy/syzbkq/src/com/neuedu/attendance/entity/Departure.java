package com.neuedu.attendance.entity;

import java.util.List;

import cn.thinking.common.page.Page;

/**
 * 离岗申请与审批的实体类
 * @author Administrator
 *
 */
public class Departure {
	private String ID;//申请离岗人员id
	private String departure_name;//申请离岗人员名字
	private String departure_department;//申请离岗人员部门
	private String departure_group;//申请离岗人员班组
	private String departure_class;//申请离岗人员班制
	private String departure_reason;//申请离岗原因
	private String departure_date;//申请离岗多久
	private String departure_time;//申请时间
	private String departure_status;//审批状态
	private Page page=new Page();
	private List<Departure> list;//因为在执行更新语句的时候前台传过来的是list，所以在这块要声明并且给出get,set方法来接收这个list
	
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getDeparture_name() {
		return departure_name;
	}
	public void setDeparture_name(String departure_name) {
		this.departure_name = departure_name;
	}
	public String getDeparture_department() {
		return departure_department;
	}
	public void setDeparture_department(String departure_department) {
		this.departure_department = departure_department;
	}
	public String getDeparture_group() {
		return departure_group;
	}
	public void setDeparture_group(String departure_group) {
		this.departure_group = departure_group;
	}
	public String getDeparture_class() {
		return departure_class;
	}
	public void setDeparture_class(String departure_class) {
		this.departure_class = departure_class;
	}
	public String getDeparture_reason() {
		return departure_reason;
	}
	public void setDeparture_reason(String departure_reason) {
		this.departure_reason = departure_reason;
	}
	public String getDeparture_date() {
		return departure_date;
	}
	public void setDeparture_date(String departure_date) {
		this.departure_date = departure_date;
	}
	public String getDeparture_time() {
		return departure_time;
	}
	public void setDeparture_time(String departure_time) {
		this.departure_time = departure_time;
	}
	public Page getPage() {
		return page;
	}
	public void setPage(Page page) {
		this.page = page;
	}
	public String getDeparture_status() {
		return departure_status;
	}
	public void setDeparture_status(String departure_status) {
		this.departure_status = departure_status;
	}
	public List<Departure> getList() {
		return list;
	}
	public void setList(List<Departure> list) {
		this.list = list;
	}
	
	
	

}
